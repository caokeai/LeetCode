/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    bool res=true;
    bool isBalanced(TreeNode* root) {
        dfs(root);
        return res;
    }
    int dfs(TreeNode* root)
    {
        if(root==nullptr)
            return 0;
        int l=dfs(root->left);
        int r=dfs(root->right);
        if(abs(r-l)>1)
            res=false;
        return max(l,r)+1;//从最深层的递归往前推 所以要用返回值
    }
};
